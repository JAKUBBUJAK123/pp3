public class Product {
    int prize;
    String name;
    String company;
    Boolean isSale;
    int amount;
    public Product(int prize, String name, String company, Boolean isSale, int amount) {
        this.prize = prize;
        this.name = name;
        this.company = company;
        this.isSale = isSale;
        this.amount = amount;
    }
}
